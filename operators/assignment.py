# Example: Assignment Operators

x = 10      # Assigns 10 to x
x += 5      # Adds 5 to x (x = x + 5)
print(x)    # Output: 15
x -= 3      # Subtracts 3 from x (x = x - 3)
print(x)   # Output: 12
x *= 2      # Multiplies x by 2 (x = x * 2)
print(x)    # Output: 24
x /= 4      # Divides x by 4 (x = x / 4)
print(x)    # Output: 6.0
x %= 5      # Modulus operation (x = x % 5)
print(x)    # Output: 1.0   
x **= 3     # Exponentiation (x = x ** 3)
print(x)    # Output: 1.0
x //= 2     # Floor division (x = x // 2)
print(x)    # Output: 0.0
x &= 1      # Bitwise AND (x = x & 1)
print(x)    # Output: 0.0